function normalizeCedula($val) {
    $clean = preg_replace('/[^0-9]/', '', $val);
    if (strlen($clean) === 11) {
        return substr($clean, 0, 3) . '-' . substr($clean, 3, 7) . '-' . substr($clean, 10, 1);
    }
    return $val;
}

$cedulas = array_unique(array_map('normalizeCedula', $raw_cedulas));

$completado_id = Estado::where('nombre', 'Completado')->value('id') ?? 9;

$stats = [
    'unique_cedulas' => count($cedulas),
    'found' => 0,
    'already_final' => 0,
    'updated' => 0,
    'not_found' => 0
];

try {
    DB::beginTransaction();

    foreach ($cedulas as $cedula) {
        $afiliados = Afiliado::where('cedula', $cedula)->get();

        if ($afiliados->isEmpty()) {
            $stats['not_found']++;
            continue;
        }

        foreach ($afiliados as $afiliado) {
            $stats['found']++;

            if ($afiliado->estado_id == $completado_id) {
                $stats['already_final']++;
                continue;
            }

            $oldEstado = $afiliado->estado_id;
            $afiliado->estado_id = $completado_id;
            $afiliado->save();

            HistorialEstado::create([
                'afiliado_id' => $afiliado->id,
                'estado_anterior_id' => $oldEstado,
                'estado_nuevo_id' => $completado_id,
                'user_id' => 1,
                'observacion' => 'Actualización masiva (Lote 2): Marcado como completado por solicitud de usuario.'
            ]);

            $stats['updated']++;
        }
    }

    DB::commit();

    echo "Reporte de Operación Lote 2:\n";
    echo "--------------------------\n";
    echo "Cédulas únicas: {$stats['unique_cedulas']}\n";
    echo "Afiliados encontrados: {$stats['found']}\n";
    echo "Ya estaban en estado final (Ignorados): {$stats['already_final']}\n";
    echo "Actualizados a Completado: {$stats['updated']}\n";
    echo "Cédulas no encontradas: {$stats['not_found']}\n";

} catch (\Exception $e) {
    DB::rollBack();
